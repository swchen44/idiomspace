export const parseCSV = (content: string): Idiom[] => {
  const lines = content.split('\n');
  const result: Idiom[] = [];

  // Regex to handle CSV with potential quotes
  // Matches: 
  // 1. Quoted string: "..."
  // 2. Non-comma characters: [^,]+
  const regex = /(".*?"|[^,]+)(?=\s*,|\s*$)/g;

  lines.forEach((line, index) => {
    if (!line.trim()) return;
    
    // Simple split won't work due to quotes containing commas.
    // However, the format is mostly `Word,Definition` or `Word,"Definition"`.
    // Let's use a manual find for the first comma to be safe and simple.
    const firstCommaIndex = line.indexOf(',');
    if (firstCommaIndex === -1) return;

    const word = line.substring(0, firstCommaIndex).trim();
    let definition = line.substring(firstCommaIndex + 1).trim();

    // Remove surrounding quotes if present
    if (definition.startsWith('"') && definition.endsWith('"')) {
      definition = definition.slice(1, -1);
    }
    
    // Fix escaped quotes if any (though typically CSV doubles them, here we just assume clean text inside quotes)
    definition = definition.replace(/""/g, '"');

    if (word && definition) {
      result.push({
        id: `id_${index}_${Math.random().toString(36).substr(2, 5)}`,
        word,
        definition
      });
    }
  });

  return result;
}

